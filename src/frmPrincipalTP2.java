    private void btnPrixvoyageMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnPrixvoyageMouseClicked
        // TODO add your handling code here:
        double prix;
            double heberg;
            double total;
            if(sldNBeleves.getValue() <= 25)
            {
                prix = 67.30;
            }
            else
            {
                 prix = 61;
            }
            if(sldNBeleves.getValue() <= 30)
            {
                heberg = 4.75;
            }
            else
            {
                if(sldNBeleves.getValue() > 35)
                {
                    heberg = 3.5;
                }
                else
                {
                    heberg = 4;   
                }
            }
            // Total = prix voyage + nourriture + hébergement
            total = (prix *sldNBeleves.getValue()) + (3.5 * sldNBeleves.getValue()) + (heberg * sldNBeleves.getValue());
            txtPrixvoyage.setText(String.valueOf(total));
    
    }//GEN-LAST:event_btnPrixvoyageMouseClicked

