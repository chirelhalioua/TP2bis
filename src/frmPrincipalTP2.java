    private void btnEchangerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEchangerMouseClicked
        // TODO add your handling code here:
        if (txtVAL1.getText().compareTo("")==0)
        {
            JOptionPane.showMessageDialog(this,"Saisir une valeur 1");
        }
            else 
        {
             if(txtVAL2.getText().compareTo("")==0)
            {
                JOptionPane.showMessageDialog(this, "Saisir une valeur 2");
            }
            else
            {
                String temp = txtVAL2.getText();
                txtVAL2.setText(txtVAL1.getText());
                txtVAL1.setText(temp);
            }
        }
            
    }//GEN-LAST:event_btnEchangerMouseClicked
