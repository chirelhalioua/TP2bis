    private void btnDessinerTriangleMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnDessinerTriangleMouseClicked
        // TODO add your handling code here:
        if(txtNBetoiles.getText().compareTo("")==0)
        {
            JOptionPane.showMessageDialog(this, "Saisir un nombre d'étoiles");
        }
        else
        {
            txtTriangle.setText("");
            for(int i = 1 ; i <= Integer.parseInt(txtNBetoiles.getText()) ; i++)
            {
                String ligne = "";
                for(int j = 1 ; j <= i  ; j++)
                {
                    
                    ligne = ligne + " * ";
                   
                }
                txtTriangle.setText(txtTriangle.getText() + ligne + "\n");
            }
        }
        
    }//GEN-LAST:event_btnDessinerTriangleMouseClicked
