    private void btnTirageAlatoireMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnTirageAlatoireMouseClicked
        // TODO add your handling code here:
         Random r = new Random();
        int nb1 = r.nextInt(21) + 1;
        int nb2 = r.nextInt(21) + 1;
        int nbCoups = 1;
        txtNombres.setText("");
        while( nb1 != nb2)
        {
            txtNombres.setText(txtNombres.getText() + "\n" + " nb1 = " + nb1 + "   nb2 = " + nb2 + "\n");
            nb1 = r.nextInt(21) + 1;
            nb2 = r.nextInt(21) + 1;
            nbCoups++;
            // Demo
        }
        txtNombres.setText(txtNombres.getText() + "\n" + " nb1 = " + nb1 + "   nb2 = " + nb2 + "\n");
        txtNBcoups.setText(String.valueOf(nbCoups));
                                      
    }//GEN-LAST:event_btnTirageAlatoireMouseClicked
        
    
    

    
    
    
    
